def a(x,y,a,color):
    b = int(a/2)
    fillcolor(color)
    begin_fill()
    goto(x,a*5+y)
    goto(a*4+x,a*5+y)
    goto(a*4+x,y)
    goto(a*3+x,y)
    goto(a*2+b+x,a*2+y)
    goto(a+b+x,a*2+y)
    goto(a+x,y)
    goto(x,y)
    end_fill()
    LinInv(a+b+x,a*3+y)
    fillcolor('white')
    begin_fill()
    goto(a+b+x,a*4+y)
    goto(a*2+b+x,a*4+y)
    goto(a*2+b+x,a*3+y)
    goto(a+b+x,a*3+y)
    end_fill()    
    return a*4 +x   
    
def LinInv(x,y):
    penup()
    goto(x,y)
    pendown ()
    
def CirInv(x,y):
    penup()
    turtle.circle(x,y)
    pendown ()

def mover(x,y,d):
    LinInv(x+d,y)
    #print("Posicion Movida:", turtle.position())    
    return x +d

