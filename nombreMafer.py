import turtle
from turtle import *
turtle.setup(1000, 500,0,0)
screensize(3000, 1000)
title("Nombre Mafer con Turtle")

def m(x,y,a,color): #a = 50 i = 0
    fillcolor(color)
    begin_fill()
    b = int(a/2)
    LinInv(x,y)
    goto(a+x,y)
    goto(a+x,a*3+y)
    goto(b*3+x,y)
    goto(b*5+x,y)
    goto(a*3+x,a*3+y)
    goto(a*3+x,y)
    goto(a*4+x,y)
    goto(a*4+x,a*5+y)
    goto(a*3+x,a*5+y)
    goto(a*2+x,a*3+y)
    goto(a+x,a*5+y)
    goto(x,a*5+y)
    goto(x,y)   
    end_fill()
    return a*4 + x
    
def r(x,y,a,color):
    fillcolor(color)
    b = int(a/2)
    begin_fill()
    goto(a+x,y)
    goto(a+x,a*2+y)
    goto(a*3+x,y)
    goto(a*4+x,y)
    goto(a*2+x,a*2+y)
    LinInv(a*2+x,a*5+y)
    turtle.circle(-(b*3),180)
    LinInv(a*2+x,a*5+y)
    goto(x,a*5+y)
    goto(x,y)
    end_fill()
    LinInv(a*2+x,a*3+y)
    goto(a*2+x+5,a*3+y)
    goto(a*2+x,a*3+y)
    CirInv(-int(a/2),180)
    fillcolor('white')
    begin_fill()
    turtle.circle(-int(a/2),180)
    end_fill()
    LinInv(a*2+x,a*4+y)
    fillcolor('white')
    begin_fill()
    goto(a+x,a*4+y)
    goto(a+x,a*3+y)
    goto(a*2+x,a*3+y)
    end_fill()
    return a*4+x
        

def f(x,y,a,color):
    b = int(a/2)
